# 리스트 내포
'''
리스트를 만드는 반복문을 짧고 간결하게 만들어 줌
[식 for 변수 in 반복가능객체]
'''


#---------------------------------------실습------------------------------------------------
num = input("출력할 단 수를 입력해주세요 : ")
num = int(num)
for i in range(1,10):
    print(num, " * ", i, " = ", num * i)

#2. 리스트 내포 이용) 2단부터 9단, 4단은 건너뛰기
l =[]
for i in range(2,10):
    for j in range(1,10):
        if(i != 4) :
            l.append(i*j)
print(l)