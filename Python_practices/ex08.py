# for 반복문
'''
리스트, 튜플 등 집합 자료형을 사용하여 요소를 하나씩 가져와 반복 가능
반복가능 객체인 문자열, 사전, 집합 자료형 이용

- 반복 객체 함수 : iterable 객체란 뜻, 시퀀스 데이터: 리스트, 튜플, 스트링
    collections, iterable 에 속한 인스턴스인지 아닌지로 반복 가능한 객체를 판별할 수 있음
- range() 함수 : 반복 가능한 객체를 반환해주는 함수 range(시작, 끝, 스텝) > 순차적 값 반환
- 인덱스로 접근 가능

continue를 활용해 한 차례 반복을 건너뛸 수 있음
'''
for i in [1,2,3,4]:
    print(i)

